import SwiftUI

extension Font {

    static func interRegular(size: CGFloat) -> Font {
        .custom("Inter24pt-Regular", size: size)
    }

    static func interMedium(size: CGFloat) -> Font {
        .custom("Inter24pt-Medium", size: size)
    }

    static func interSemiBold(size: CGFloat) -> Font {
        .custom("Inter24pt-SemiBold", size: size)
    }
}
